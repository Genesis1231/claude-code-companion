"""PostToolUse hook: when a Bash command fails, speak a warm, in-character line
reacting to it — a dash of sympathy or playful encouragement. The line is
generated by a nested headless `claude -p` from the persona + failure_prompt in
config.json (plus the failed command and a snippet of its error), then sent to
the resident daemon.

Contextual but cheap: a cooldown keeps bursts of failures from nagging, and the
cooldown is checked BEFORE the (expensive) claude call. Async and fail-silent —
any problem just means no voice, never a broken session.

Recursion guard: the nested `claude -p` is itself a Claude Code session and runs
tools that re-trigger this hook. We launch it with COMPANION_NO_HOOK=1 and bail
immediately when we see that, so the inner Claude never reacts to its own tools.
"""

import json
import os
import shutil
import subprocess
import sys
import time
import urllib.request
from pathlib import Path

if os.environ.get("COMPANION_NO_HOOK"):
    sys.exit(0)  # nested claude -p invocation — do nothing

HERE = Path(__file__).parent
_cfg = json.loads((HERE / "config.json").read_text())
PORT = _cfg["port"]
VOICE = _cfg["voice"]
PERSONA = _cfg["persona"]
FAILURE_PROMPT = _cfg["failure_prompt"]

# Don't react more than once per window — failures come in bursts and a line per
# failure would nag. Generous on purpose; tune here if it feels too quiet/chatty.
COOLDOWN_S = 180
STAMP = Path(os.environ.get("TMPDIR", "/tmp")) / "claude-code-companion" / ".last_failure_react"

# Commands whose failures print to stdout, not stderr — react to these even when
# stderr is empty (otherwise the stderr filter below would miss them).
_TEST_HINTS = ("pytest", "npm test", "npm run", "go test", "cargo test", "make ", " test ")

# Bypass any system proxy — the daemon is on localhost.
_opener = urllib.request.build_opener(urllib.request.ProxyHandler({}))


def _daemon_ready() -> bool:
    try:
        with _opener.open(f"http://127.0.0.1:{PORT}/health", timeout=1) as r:
            return json.loads(r.read()).get("ready") is True
    except Exception:
        return False


def _claude_bin():
    """Resolve the claude CLI. Hooks can run with a minimal PATH (GUI/launchd
    launch), so fall back to common install locations before giving up."""
    found = shutil.which("claude")
    if found:
        return found
    for p in (Path.home() / ".local/bin/claude", Path("/opt/homebrew/bin/claude"),
              Path("/usr/local/bin/claude"), Path.home() / ".claude/local/claude"):
        if p.exists():
            return str(p)
    return None


def _failed(payload: dict) -> bool:
    """Did a Bash command genuinely fail? Defensive about the tool_response shape
    (field names aren't guaranteed), and suppresses quiet non-zero exits like
    `grep` finding nothing — unless the command looks like a test/build."""
    if payload.get("tool_name") != "Bash":
        return False
    resp = payload.get("tool_response")
    if not isinstance(resp, dict):
        return False
    code = resp.get("exit_code", resp.get("returncode"))
    nonzero = isinstance(code, int) and code != 0
    flagged = bool(resp.get("is_error")) or resp.get("success") is False
    if not (nonzero or flagged):
        return False
    cmd = ((payload.get("tool_input") or {}).get("command") or "")
    if any(h in cmd for h in _TEST_HINTS):
        return True
    # Otherwise require real stderr, so silent exit-1s (grep, test -f) stay quiet.
    return len((resp.get("stderr") or "").strip()) > 20


def _cooling_down() -> bool:
    try:
        return (time.time() - STAMP.stat().st_mtime) < COOLDOWN_S
    except OSError:
        return False


def _touch() -> None:
    try:
        STAMP.parent.mkdir(parents=True, exist_ok=True)
        STAMP.write_text(str(time.time()))
    except OSError:
        pass


def main():
    try:
        payload = json.load(sys.stdin)
    except (json.JSONDecodeError, ValueError):
        return
    # Cheap checks first, network/claude last.
    if not _failed(payload) or _cooling_down():
        return
    claude = _claude_bin()
    if not claude or not _daemon_ready():
        return
    _touch()  # claim the cooldown window before the slow call

    resp = payload.get("tool_response") or {}
    cmd = ((payload.get("tool_input") or {}).get("command") or "")[:300]
    err = ((resp.get("stderr") or resp.get("stdout") or "").strip())[:500]
    prompt = f"{PERSONA}\n\n{FAILURE_PROMPT}\n\nCommand:\n{cmd}\n\nError:\n{err}"
    try:
        line = subprocess.run(
            [claude, "-p", prompt],
            capture_output=True, text=True, timeout=30,
            env={**os.environ, "COMPANION_NO_HOOK": "1"},
        ).stdout.strip()
    except (subprocess.SubprocessError, OSError):
        return
    if not line:
        return

    try:
        req = urllib.request.Request(
            f"http://127.0.0.1:{PORT}/speak",
            data=json.dumps({"text": line, "voice": VOICE}).encode(),
            headers={"Content-Type": "application/json"},
            method="POST",
        )
        _opener.open(req, timeout=2)  # fire-and-forget
    except Exception:
        pass


if __name__ == "__main__":
    main()
