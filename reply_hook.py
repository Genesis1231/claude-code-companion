"""UserPromptSubmit hook: speak a warm companion line reacting to the user's
message. The line is generated by a nested headless `claude -p` from the shared
persona + reply_prompt in config.json, then sent to the resident daemon.

It fires the instant the user submits, so the line generates and plays WHILE
Claude composes its reply — overlapping the wait instead of trailing it. Async
and fail-silent: any problem just means no voice, never a broken session.

Recursion guard: the nested `claude -p` is itself a Claude Code session and
would re-trigger this hook (and SessionStart/SessionEnd). We launch it with
COMPANION_NO_HOOK=1 and bail immediately when we see that — so the inner Claude
never spawns more Claudes or tears the daemon down.
"""

import datetime
import json
import os
import shutil
import subprocess
import sys
import urllib.request
from pathlib import Path

if os.environ.get("COMPANION_NO_HOOK"):
    sys.exit(0)  # nested claude -p invocation — do nothing

from config import PORT, VOICE, PERSONA, REPLY_PROMPT, LOGS_DIR, session_log, logger

# A turn is a user line plus a Claude entry; a long Claude reply spans 3 physical
# lines (head / elision marker / tail), so 12 lines ≈ the most recent 3 turns.
_MAX_LINES = 12


def _read_context(session_id: str) -> str:
    """Return the recent turns as a context snippet (oldest first), or empty.
    Keeps continuation lines of multi-line entries — only blank lines are dropped."""
    if not session_id:
        return ""
    try:
        lines = [l for l in session_log(session_id).read_text().splitlines() if l.strip()]
        return "\n".join(lines[-_MAX_LINES:])
    except OSError:
        return ""


def _append_message(session_id: str, msg: str) -> None:
    """Append the user message to the session log (fail-silent)."""
    if not session_id:
        return
    try:
        LOGS_DIR.mkdir(parents=True, exist_ok=True)
        ts = datetime.datetime.now().strftime("%H:%M")
        msg = " ".join(msg.split())  # flatten newlines so it stays one log line
        with session_log(session_id).open("a") as fp:
            fp.write(f"- [{ts}] User: {msg}\n")
    except OSError:
        logger.warning("failed to append message to session log", exc_info=True)


# Bypass any system proxy — the daemon is on localhost.
_opener = urllib.request.build_opener(urllib.request.ProxyHandler({}))


def _daemon_ready() -> bool:
    try:
        with _opener.open(f"http://127.0.0.1:{PORT}/health", timeout=1) as r:
            return json.loads(r.read()).get("ready") is True
    except Exception:
        return False  # daemon simply not up yet — expected, stay quiet


def _claude_bin():
    """Resolve the claude CLI. Hooks can run with a minimal PATH (GUI/launchd
    launch), so fall back to common install locations before giving up."""
    found = shutil.which("claude")
    if found:
        return found
    for p in (Path.home() / ".local/bin/claude", Path("/opt/homebrew/bin/claude"),
              Path("/usr/local/bin/claude"), Path.home() / ".claude/local/claude"):
        if p.exists():
            return str(p)
    return None


def main():
    try:
        payload = json.load(sys.stdin)
    except (json.JSONDecodeError, ValueError):
        return
    user_msg = (payload.get("prompt") or "").strip()
    session_id = (payload.get("session_id") or "").strip()
    if not user_msg:
        return

    # Log the user line independently of the voice pipeline: read context BEFORE
    # appending (so it excludes the current message), then record it. The Stop
    # hook logs Claude's reply unconditionally, so the user side must too — else a
    # slow/absent daemon (model still loading, or claude -p timing out) leaves
    # Claude lines in the log with no matching user line.
    context = _read_context(session_id)
    _append_message(session_id, user_msg)

    # The spoken reaction is best-effort; bail quietly if we can't make one.
    claude = _claude_bin()
    if not claude or not _daemon_ready():
        return

    if context:
        prompt = (f"{PERSONA}\n\n{REPLY_PROMPT}\n\n"
                  f"Recent conversation between user and AI Coder(not you):\n{context}\n\n"
                  f"User asks the AI Coder:\n{user_msg}")
    else:
        prompt = f"{PERSONA}\n\n{REPLY_PROMPT}\n\nUser asks the AI Coder:\n{user_msg}"
    try:
        line = subprocess.run(
            # --strict-mcp-config: load no MCP servers, so this text-only call
            # can't reach the project's `speak` tool and narrate a permission
            # denial into the spoken line.
            [claude, "-p", "--strict-mcp-config", prompt],
            capture_output=True, text=True, timeout=30,
            env={**os.environ, "COMPANION_NO_HOOK": "1"},
        ).stdout.strip()
    except (subprocess.SubprocessError, OSError):
        logger.exception("claude -p failed to generate a reply line")
        return
    if not line:
        return

    try:
        req = urllib.request.Request(
            f"http://127.0.0.1:{PORT}/speak",
            data=json.dumps({"text": line, "voice": VOICE}).encode(),
            headers={"Content-Type": "application/json"},
            method="POST",
        )
        _opener.open(req, timeout=2)  # fire-and-forget
    except Exception:
        logger.warning("failed to POST the reply line to the daemon", exc_info=True)


if __name__ == "__main__":
    try:
        main()
    except Exception:
        # Catch-all so a crash (e.g. a dropped import) is recorded, not silent.
        logger.exception("reply_hook crashed")
